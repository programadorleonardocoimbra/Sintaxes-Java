package Conteúdo;
// Variáveis armazenam valores na memória:

// Variável "int" = Números Inteiros (ex: 10);
// Variável "double" = Números Decimais (ex: 3,14);
// Variável "char" = Caracteres (ex: A);
// Variável "boolean" = Verdadeiro ou Falso (True ou False);
// Variável "byte" = Inteiro Pequeno (ex: 127);
// Variável "short" = Inteiro Curto (ex: 1000);
// Variável "long" = Inteiro Grande (ex: 100000L);
// Variável "float" = Decimal (ex: 5.5f);

// Exemplos de Variáveis do tipo PRIMITIVO:


public class tiposEVariaveis {
    public static void main(String[] args) {

        
        //variáveis PRIMITIVAS:
        int idade = 25;
        double altura = 1.75;
        char letra = 'L';
        boolean estudante = true;

        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Letra: " + letra);
        System.out.println("Estudante: " + estudante);


    }

}
